//increment and decrement operators
#include <iostream>
using namespace std;
int main(){
        int x=5;
        cout<<x<<endl;
        ++x;                 //pre increment  x=x+1
        cout<<x<<endl;
        x++;                //post increment x=x+1
        cout<<x<<endl;
        x--;               //post decrement x=x-1
        cout<<x<< endl; 



 


}